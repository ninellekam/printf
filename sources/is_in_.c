#include "../includes/ft_printf.h"

int		is_in_type_list(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'p') || (c == 'd') || (c == 'i')
			|| (c == 'u') || (c == 'x') || (c == 'X') || (c == '%'));
}

int		is_in_flags_list(int c)
{
	if (c == ' ')
		ft_putchar(' ');
	return ((c == '-') || (c == ' ') || (c == '0') || (c == '.') || (c == '*'));
}
